

<h2 class="pageTitle">About Grandlord</h2>


<div  id="aboutContent">

   <p>Grandlord website has been developed by three CIT students in 2014/2015 academic year as part of a student project.</p>
   <p>The idea to create this website has been inspired by real life experiences and most of all lack of information about landlords who are renting properties.</p>
   <br>
   <p>At the time of creating this project there is no website or system that enables tenants to check reviews on properties,
   landlords and agencies that offer accommodation to rent in Ireland.</p>
   <br>
   <p>Many tenants would like to see other tenants opinions about landlord or agency advertising accommodation to rent.</p>
   <p>Lack of information about landlords and properties causes unnecessary risk and uncertainty before committing to live in certain place for the time specified in a lease contract.
   In order to enable tenants to check reviews on properties there is a system required that will gather all relevant information for future reference.
   We believe that GrandLord System will enable all interested parties to seek required information about properties and owners helping everyone to meet their expectations.</p>

