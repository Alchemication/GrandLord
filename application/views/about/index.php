

<h3> Grandlord - Software Project - CIT 2015</h3>

<div class="form-group">
<p> At the time of writing this report there is no website or system that enable tenants to check reviews on properties,  landlords and agencies that offer accommodation to rent in Ireland.</p>

   <h4> Many tenants would like to see other tenants opinions about landlord or agency advertising accommodation to rent. Lack of information about landlords and properties causes unnecessary risk and uncertainty before committing to live in certain place for the time specified in a lease contract.  In order to enable tenants to check reviews on properties there is a system required that will gather all required information for future reference of other users.  We believe that GrandLord System will enable all interested parties to seek required information about properties and owners helping everyone to meet their expectations. </h4>

